    private List<ComplexUser> buildComplexUsers(int count) {
        List<ComplexUser> users = new LinkedList<>();
        for (int i = 0; i < count; i++) {
            users.add(this.buildComplexUser());
        }
        return users;
    }

    private String insertAndGetComplexUserId() {
        ComplexUser user = this.buildComplexUser();
        this.sqlSession.getMapper(ComplexUserMapper.class).insert(user);
        this.sqlSession.commit();
        return user.getId();
    }

    private List<String> insertAndGetComplexUserIds(int count) {
        List<ComplexUser> users = this.buildComplexUsers(count);
        this.sqlSession.getMapper(ComplexUserMapper.class).batchInsert(users);
        this.sqlSession.commit();
        List<String> ids = new LinkedList<>();
        for (ComplexUser user : users) {
            ids.add(user.getId());
        }
        return ids;
    }

