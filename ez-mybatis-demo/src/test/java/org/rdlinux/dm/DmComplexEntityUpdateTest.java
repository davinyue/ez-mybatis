    @Test
    public void ezUpdateByLambdaDSL() {
        EzMapper mapper = this.sqlSession.getMapper(EzMapper.class);
        EntityTable table = EntityTable.of(ComplexUser.class);
        String userId = this.insertAndGetComplexUserId();

        EzUpdate ezUpdate = EzUpdate.update(table)
                .set(s -> {
                    s.add(table.field(ComplexUser.Fields.username).set("Lambda Update API"));
                    // Conditional addition to the SET fragment
                    s.add(true, table.field(ComplexUser.Fields.age).set(99));
                })
                .where(w -> w.add(table.field(BaseEntity.Fields.id).eq(userId)))
                .build();

        int updated = mapper.ezUpdate(ezUpdate);
        this.sqlSession.commit();
        Assert.assertTrue(updated > 0);

        ComplexUser user = mapper.selectById(ComplexUser.class, userId);
        Assert.assertEquals("Lambda Update API", user.getUsername());
        Assert.assertEquals(Integer.valueOf(99), user.getAge());
        log.info("ezUpdateByLambdaDSL result: {}", updated);
    }

