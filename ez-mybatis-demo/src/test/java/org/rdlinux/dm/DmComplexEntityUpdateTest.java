    @Test
    public void ezUpdateFormulaTest() {
        try {
            EzMapper mapper = this.sqlSession.getMapper(EzMapper.class);
            EntityTable table = EntityTable.of(ComplexUser.class);
            String uId = this.insertAndGetComplexUserId();

            Formula scoreFormula = Formula.build(f ->
                    f.with(table.field(ComplexUser.Fields.score))
                            .addGroup(g ->
                                    g.with(table.field(ComplexUser.Fields.age)).subtract(5L))
                            .subtractGroup(g ->
                                    g.with(table.field(ComplexUser.Fields.age)).subtract(5L))
                            .divideGroup(g ->
                                    g.with(table.field(ComplexUser.Fields.age)).subtract(5L))
                            .multiplyGroup(g ->
                                    g.with(table.field(ComplexUser.Fields.age)).subtract(5L))
                            .add(1)
                            .subtract(1)
                            .multiply(1)
                            .divide(1)
                            .add(1)
            );
            Formula accountBalanceFormula = Formula.build(f ->
                    f.with(1)
            );
            EzUpdate ezUpdate = EzUpdate.update(table)
                    .set(s ->
                            s.add(table.field(ComplexUser.Fields.score).set(scoreFormula))
                                    .add(table.field(ComplexUser.Fields.accountBalance).set(accountBalanceFormula)))
                    .where(w -> w.add(table.field(BaseEntity.Fields.id).eq(uId)))
                    .build();
            int ret = mapper.ezUpdate(ezUpdate);
            this.sqlSession.commit();
            Assert.assertEquals(1, ret);
        } catch (Exception e) {
            this.sqlSession.rollback();
            throw new RuntimeException(e);
        }
    }

