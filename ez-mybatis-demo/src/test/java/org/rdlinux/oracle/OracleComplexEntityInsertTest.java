    @Test
    public void ezMapperInsertByQuery() {
        EzMapper mapper = this.sqlSession.getMapper(EzMapper.class);
        // 先插入一条种子订单数据，确保 SELECT 有结果可以被归档
        ComplexOrder seed = this.buildComplexOrder();
        mapper.insert(seed);

        // 构建查询：选取归档源表 (订单表) 中的记录，最多 1 条
        EntityTable orderTable = EntityTable.of(ComplexOrder.class);
        EzQuery<ComplexOrder> query = EzQuery.builder(ComplexOrder.class)
                .from(orderTable)
                .select(Select.EzSelectBuilder::addAll)
                .where(e -> e.add(orderTable.field(BaseEntity.Fields.id).eq(seed.getId())))
                .build();

        // 目标写入表设定为新建立的订单归档表：ez_complex_order_archive
        EntityTable archiveTargetTable = EntityTable.of(ComplexOrder.class, "EZ_COMPLEX_ORDER_ARCHIVE");

        int ret = mapper.insertByQuery(archiveTargetTable, query);
        this.sqlSession.commit();
        Assert.assertTrue(ret >= 0);
        log.info("EzMapper.insertByQuery (Archive Order) ret: {}", ret);
    }

