    @Test
    public void nativeJdbcBatchInsertPerformanceTest() throws SQLException {
        Connection connection = this.sqlSession.getConnection();
        String sql = "INSERT INTO ez_complex_user ( id, create_time, update_time, username, age, user_type, " +
                "score, account_balance, salary, is_active, status, birthday, " +
                "custom_column_name, secret_content, ext_info, department_id ) " +
                "VALUES ( ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ? )";

        long start = System.currentTimeMillis();
        for (int h = 0; h < 1; h++) {
            PreparedStatement statement = connection.prepareStatement(sql);
            EzMapper mapper = this.sqlSession.getMapper(EzMapper.class);
            this.preheat(mapper);
            for (int i = 0; i < 1; i++) {
                String id = UUID.randomUUID().toString().replace("-", "");
                // jdbc压测中由于走不到 AbstractBaseTest 的拦截器，所以此处原生语句需要提供时间和ID赋值
                statement.setString(1, id);
                statement.setTimestamp(2, new java.sql.Timestamp(System.currentTimeMillis()));
                statement.setTimestamp(3, new java.sql.Timestamp(System.currentTimeMillis()));
                statement.setString(4, "PerfUser_" + i);
                statement.setInt(5, 30);
                statement.setShort(6, (short) 1);
                statement.setLong(7, 100L);
                statement.setDouble(8, 99.99);
                statement.setBigDecimal(9, new BigDecimal("123.45"));
                statement.setBoolean(10, true);
                statement.setShort(11, (short) 0);
                statement.setDate(12, new java.sql.Date(System.currentTimeMillis()));
                statement.setString(13, "test_col");
                statement.setString(14, "ENCRYPTED::pass");
                statement.setString(15, "{\"hobby\":\"Coding\"}");
                statement.setString(16, "Dept01");
                statement.addBatch();
            }
            statement.executeBatch();
            statement.close();
        }
        connection.commit();
        connection.close();
        long end = System.currentTimeMillis();
        this.sqlSession.commit();
        Assert.assertTrue(end - start >= 0);
        log.info("nativeJdbcBatchInsertPerformanceTest cost: {}ms", end - start);
    }

