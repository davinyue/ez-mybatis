        EntityTable userTable = EntityTable.of(ComplexUser.class);

        EzDelete delete1 = EzDelete.delete(userTable)
                .where(e -> e.addCondition(userTable.field(BaseEntity.Fields.id).eq(ids.get(0))))
                .build();
        deletes.add(delete1);

