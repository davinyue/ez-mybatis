    @Test
    public void ezDelete() {
        ComplexDepartment dept = new ComplexDepartment();
        dept.setName("Test_Dept_For_Delete");
        this.sqlSession.getMapper(EzMapper.class).insert(dept);

        ComplexUser user = this.buildComplexUser();
        user.setDepartmentId(dept.getId());
        this.sqlSession.getMapper(ComplexUserMapper.class).insert(user);
        this.sqlSession.commit();

        EntityTable userTable = EntityTable.of(ComplexUser.class);
        EntityTable deptTable = EntityTable.of(ComplexDepartment.class);

        EzDelete delete = EzDelete.delete(userTable).delete(deptTable)
                .join(deptTable, e ->
                        e.addCondition(userTable.field(ComplexUser.Fields.departmentId)
                                .eq(deptTable.field(BaseEntity.Fields.id))))
                .where(e ->
                        e.addCondition(userTable.field(BaseEntity.Fields.id).eq(user.getId())))
                .build();
        int ret = this.sqlSession.getMapper(EzMapper.class).ezDelete(delete);
        this.sqlSession.commit();
        Assert.assertTrue(ret >= 0);
        log.info("Test ezDelete - Deleted {} records using JOIN", ret);
    }

